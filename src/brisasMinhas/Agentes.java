package brisasMinhas;

public class Agentes {

    String nome;
    String tipo;
    String nacionalidade;
    String habilidadeAssinatura;
    String ultimate;

    Agentes (String nome, String tipo,
             String nacionalidade,
             String habilidadeAssinatura,
             String ultimate) {
        this.nome = nome;
        this.tipo = tipo;
        this.nacionalidade = nacionalidade;
        this.habilidadeAssinatura = habilidadeAssinatura;
        this.ultimate = ultimate;
    }

}
